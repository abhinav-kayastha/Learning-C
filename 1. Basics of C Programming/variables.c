#include <stdio.h>

int main()
{
    /*
    variable syntax:

    data_type variable_name = value; // defining single variable
    data_type var1, var 2; // assigning multiple variables
    */

    /*
   data types:

   int: integer variable
   char:  1 character variable
   float: floating point variables (decimals)
    */

    /*
   variable definition:
   int num;

   variable initialization:
   var = 10;

   variable declaration and defintion:
   int var = 10;
   */

    /*
    constant variables; variables where their value cannot change;
    const int num = 10;
    */

    const char letter_a = 'a';

    int age;

    age = 19;

    float height = 178.5;

    return 0;
}